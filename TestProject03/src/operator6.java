
public class operator6 {

	public static void main(String[] args) {
		// 四則演算、剰余
		//浮動小数点型には、floatの他にdoubleというものもあります。
		//doubleの方がfloatよりも精度の高い浮動小数を求める事ができます。
		
		//floatの場合
		float a1 = (float)10 / 3;
		System.out.println(a1 + "\n");
		
		//doubleの場合
		double a2 = (double)10 / 3;
		System.out.println(a2);

	}

}
